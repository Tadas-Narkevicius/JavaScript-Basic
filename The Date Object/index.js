
// The Date Object.

var birthday = new Date(1985, 2, 5, 10, 30, 15);


document.write(birthday.getMonth() + "<br>");

document.write(birthday.getFullYear() + "<br>");

document.write(birthday.getDate() + "<br>");

document.write(birthday.getDay() + "<br>");

document.write(birthday.getHours() + "<br>");

document.write(birthday.getTime() + "<br>");