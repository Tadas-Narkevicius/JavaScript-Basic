
//Logical Operators.

// AND (&&)  true if ALL are true
var myAge = 15;

if (myAge >= 18 && myAge <= 30){
    document.write("you can come.");
} else {
    document.write("you can not come.");
}


// OR (||)  true if ONE are true
var myAge = 15;

if (myAge >= 18 || myAge <= 30){
    document.write("you can come.");
} else {
    document.write("you can not come.");
}
